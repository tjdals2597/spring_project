function js_topnav_click(no) {
	if (no == 1) {
		location.href = "./admin_list.do";
	}
	else if (no == 2) {
		location.href = "./admin_main.do";
	}
	else if (no == 3) {
		location.href = "./product_list.do";
	}
	else if (no == 4) {
		location.href = "./admin_siteinfo.do";
	}
	else if (no == 5) {
		location.href = "./notice_list.do";
	}
}